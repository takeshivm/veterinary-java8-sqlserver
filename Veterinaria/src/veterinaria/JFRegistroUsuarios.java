    private void limpiarControles(){
        this.txtNombres.setText("");
        this.txtApellidos.setText("");
        this.txtUsuario.setText("");
        this.txtContraseña.setText("");
        this.txtContraseña.setText("");
        this.rbActivo.setSelected(true);
    }
    
    private void activarControles(boolean estado) {
        this.panRegistrarUsuario.setEnabled(estado);
        this.lblNombres.setEnabled(estado);
        this.txtNombres.setEnabled(estado);
        this.lblApellidos.setEnabled(estado);
        this.txtApellidos.setEnabled(estado);
        this.lblUsuario.setEnabled(estado);
        this.txtUsuario.setEnabled(estado);
        this.lblContraseña.setEnabled(estado);
        this.txtContraseña.setEnabled(estado);
        this.btnAceptar.setEnabled(estado);
        this.btnCancelar.setEnabled(estado);
        this.rbActivo.setEnabled(estado);
        this.rbInactivo.setEnabled(estado);
        
        this.panListado.setEnabled(!estado);
        this.btnListar.setEnabled(!estado);
        this.tblListado.setEnabled(!estado);
        this.btnNuevo.setEnabled(!estado);
        this.btnModificar.setEnabled(!estado);
        this.btnCerrar.setEnabled(!estado);
        
        if (estado == true) {
            this.txtNombres.requestFocusInWindow();
        }else{
            this.tblListado.requestFocusInWindow();
        }
    }

    private void crearColumnas() {
        this.modelo.addColumn("Nombre Completo");
        this.modelo.addColumn("Usuario");
        this.modelo.addColumn("Estado");
    }

    private void presentarDatos() {
        DAOUsuario dao = new DAOUsuario();
        try {
            this.actual = dao.leer(this.actual);
            this.txtNombres.setText(this.actual.getNombres());
            this.txtApellidos.setText(this.actual.getApellidos());
            this.txtUsuario.setText(this.actual.getUsuario());
            this.txtContraseña.setText(this.actual.getContraseña());
            if (this.actual.isEstado() == true) {
                this.rbActivo.setSelected(true);
            }else{
                this.rbInactivo.setSelected(true);
            }
            
            this.activarControles(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se encontro el usuario");
        }finally{
            dao = null;
        }
    }
