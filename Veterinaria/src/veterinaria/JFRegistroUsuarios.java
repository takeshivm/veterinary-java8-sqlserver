    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        DAOUsuario dao;
        RegistroUsuario usr;
        
        if (this.validarDatos() == true) {
            usr = new RegistroUsuario();
            usr.setNombres(this.txtNombres.getText());
            usr.setApellidos(this.txtApellidos.getText());
            usr.setUsuario(this.txtUsuario.getText());
            usr.setContraseña(this.txtContraseña.getText());
            
            if (this.rbActivo.isSelected() == true) {
                usr.setEstado(true);
            }else{
                usr.setEstado(false);
            }
            
            dao = new DAOUsuario();
            try {
                if (this.actual == null) {
                    dao.registrar(usr);
                }else{
                    usr.setCodigo(this.actual.getCodigo());
                    dao.actualizar(usr);
                }
                this.activarControles(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }finally{
                dao = null;
                usr = null;
            }
        }else{
            JOptionPane.showMessageDialog(this, "Llene todos los campos porfavor");
        }
        
    }//GEN-LAST:event_btnAceptarActionPerformed

    private boolean validarDatos(){
        boolean validar = true;
        if (this.txtNombres.getText().length() == 0) {
            validar = false;
        }
        if (this.txtApellidos.getText().length() == 0) {
            validar = false;
        }
        if (this.txtUsuario.getText().length() == 0) {
            validar = false;
        }
        if (this.txtContraseña.getText().length() == 0) {
            validar = false;
        }
        
        return validar;
    }
    
    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        DAOUsuario dao = new DAOUsuario();
        
        try {
            this.usuarios = dao.listar();
            this.llenarTabla();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }finally{
            dao = null;
        }
    }//GEN-LAST:event_btnListarActionPerformed

    private void llenarTabla(){
        this.modelo.setRowCount(0);
        if (this.usuarios != null) {
            for (RegistroUsuario usuario : usuarios) {
                this.modelo.addRow(new Object[]{
                    usuario.getNombreCompleto(),
                    usuario.getUsuario(),
                    usuario.getNombreEstado()
                });
            }
        }
    }
    
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int pos = this.tblListado.getSelectedRow();
        if (pos > -1) {
            this.actual = this.usuarios.get(pos);
            this.presentarDatos();
        }else{
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

