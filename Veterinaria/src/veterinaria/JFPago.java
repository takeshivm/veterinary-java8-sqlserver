    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        JDDetallePago ventana = new JDDetallePago(this, true);
        DetallePago det;
        
        det = ventana.agregar();
        ventana.dispose();
        if (det != null) {
            this.detalles.add(det);
            this.modelo.addRow(new Object[]{
                det.getServicio().getNombre(),
                det.getPrecio()
            });
            
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private Double getIGV(){
        
        double suma = 0;
        for (DetallePago detalle : detalles) {
            suma = suma + (detalle.getPrecio()*0.18);
        }
        
        return suma;
    }
    private Double getTotal(){
        
        double total = 0;
        for (DetallePago detalle : detalles) {
            
            total = total + detalle.getPrecio();
        }
        
        return total;
    }
    
    private Double operacionTotal(){
        return this.getIGV() + getTotal();
    }
    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        DAOPago dao = new DAOPago();
        Pago pago;
        
        if (this.validarDatos() == true) {
            pago = new Pago();
            
            pago.setFecha(this.dchFecha.getDate());
           
            pago.setHistotial(this.historia);
            /*pago.setHistotial(this.historias.get(
                this.cboPropietario.getSelectedIndex()));*/
            pago.setTipoPago(this.cboDocumento.getSelectedItem().toString()
                .substring(0, 1));
            if (this.cboDocumento.getSelectedIndex() == 1) {
                pago.setIGV(0.00);
                pago.setTotal(this.getTotal());
            }else if (this.cboDocumento.getSelectedIndex() == 2) {
                pago.setIGV(this.getIGV());
                pago.setTotal(this.operacionTotal());
            }else{
                JOptionPane.showMessageDialog(this, "Seleccione el tipo de documento");
            }
            
            pago.setVigencia(this.chkVigencia.isSelected());
            pago.setDetalle(this.detalles);
            
            dao = new DAOPago();
            try {
                dao.registrar(pago);
                JOptionPane.showMessageDialog(this, "!Venta realizada con ExitoÂ¡");
                this.limpiarControles();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }finally{
                pago = null;
                dao = null;
                this.historia = null;
            }
            
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

    private void limpiarControles(){
        this.txtBuscarPaciente.setText("");
        this.txtPropietario.setText("");
        this.cboDocumento.setSelectedIndex(-1);
        this.tblDetalle.removeAll();
        this.modelo.setRowCount(0);
    }
    
    private void cboDocumentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboDocumentoActionPerformed
        int pos = this.cboDocumento.getSelectedIndex();

        if (pos == 1) {
            this.txtIGV.setText("S/."+0.00);
            this.txtTotal.setText("S/."+String.format("%.2f", this.getTotal()));
        }else if(pos == 2){
            this.txtIGV.setText("S/."+String.format("%.2f", this.getIGV()));
            this.txtTotal.setText("S/."+this.operacionTotal());
        }else{    
            this.txtIGV.setText("S/."+0.00);
            this.txtTotal.setText("S/."+0.00);
        }
    }//GEN-LAST:event_cboDocumentoActionPerformed

