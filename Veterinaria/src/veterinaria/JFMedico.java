    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        DAOMedico dao;
        Medico medico;
        
        if (this.validarDatos() == true) {
            medico = new Medico();
            medico.setNombre(this.txtNombre.getText());
            medico.setApellido(this.txtApellido.getText());
            medico.setEspecialidad(this.txtEspecialidad.getText());
            medico.setEstado(this.chkEstado.isSelected());
            
            dao = new DAOMedico();
            try {
                if (this.actual == null) {
                    dao.registrar(medico);
                }else{
                    medico.setCodigo(this.actual.getCodigo());
                    dao.actualizar(medico);
                }
                this.activarControles(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "Ingrese el nombre y apellido porfavor\n"
                    + ":D");
        }
        
    }//GEN-LAST:event_btnAceptarActionPerformed

    private boolean validarDatos(){
        boolean activar = true;
        if (this.txtNombre.getText().length() == 0 && this.txtApellido.getText().length() == 0) {
            activar = false;
        }
    
        return activar;
    }
    
    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        this.actual = null;
        this.activarControles(true);
        this.limpiarControles();
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DAOMedico dao = new DAOMedico();
        try {
            this.medicos = dao.listar(this.txtMedico.getText());
            this.llenarTabla();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }finally{
            dao = null;
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int pos = this.tblListado.getSelectedRow();
        
        if (pos > -1) {
            this.actual = this.medicos.get(pos);
            this.mostrarDatos();
        }else{
            JOptionPane.showMessageDialog(this, "Debe seleccionar un medico");
            this.tblListado.requestFocusInWindow();
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void llenarTabla(){
        this.modelo.setRowCount(0);
        
        for (Medico medico : medicos) {
            this.modelo.addRow(new Object[]{
                medico.getNombre(),
                medico.getEspecialidad(),
                medico.nombreEstado(),
            });
            
        }
    }
    
    private void mostrarDatos(){
        DAOMedico dao = new DAOMedico();
        try {
            this.actual = dao.leer(this.actual);
            if (this.actual != null) {
                this.txtNombre.setText(this.actual.getNombre());
                this.txtApellido.setText(this.actual.getApellido());
                this.txtEspecialidad.setText(this.actual.getEspecialidad());
                if (this.actual.isEstado() == true) {
                    this.chkEstado.setSelected(true);
                }else{
                    this.chkEstado.setSelected(false);
                }
                this.activarControles(true);
            }else{
                JOptionPane.showMessageDialog(this, "No se encontro el Medico");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }finally{
            dao = null;
        }
    }
    
    private void limpiarControles(){
        this.txtNombre.setText("");
        this.txtApellido.setText("");
        this.txtEspecialidad.setText("");
        this.chkEstado.setSelected(true);
    }
    
